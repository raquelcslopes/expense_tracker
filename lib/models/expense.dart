import 'package:flutter/material.dart';
import 'package:uuid/uuid.dart';
import 'package:intl/intl.dart';

final formatter = DateFormat.yMd();
const uuid = Uuid();

enum Category { house, travel, health, leisure }

const CategoryIcons = {
  Category.house: Icons.house,
  Category.travel: Icons.airplane_ticket,
  Category.health: Icons.health_and_safety,
  Category.leisure: Icons.add_reaction_rounded,
};

class Expense {
  Expense({
    required this.title,
    required this.amount,
    required this.date,
    required this.category,
  }) : id = uuid.v4(); //generates a unique id

  final String id;
  final String title;
  final double amount;
  final DateTime date;
  final Category category;

  String get formattedDate {
    return formatter.format(date);
  }
}
